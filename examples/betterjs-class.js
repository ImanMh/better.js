var Bjs	= Bjs	|| require('../build/better.js')


//////////////////////////////////////////////////////////////////////////////////
//		comment								//
//////////////////////////////////////////////////////////////////////////////////

var Cat	= function(name, weight){
	this.name	= name
	this._weight	= weight || 0
}

Cat.prototype.getWeight	= function(){
	return this._weight
}

//////////////////////////////////////////////////////////////////////////////////
//		Make a Better.js for Cat					//
//////////////////////////////////////////////////////////////////////////////////

Cat	= Bjs.Class(Cat, {
	arguments	: [String, [Number, undefined]],
	privatize	: true,
	properties	: {
		name	: String,
		_weight	: Number,
	},
})


